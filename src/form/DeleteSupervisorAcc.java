    public void Delete() throws SQLException
    {
        String supervisor = comboUser.getSelectedItem().toString();
        User user = new User();
        Code code = new Code();
        Disease disease = new Disease();
        GrassMedicinePill  medicinepill = new GrassMedicinePill();
        GrassMedicinePotion medicinepotion = new GrassMedicinePotion();
        Patient patient = new Patient();
        TraditionalMedicinePill traditionalpill = new TraditionalMedicinePill();
        TraditionalMedicinePotion traditionalpotion = new TraditionalMedicinePotion();
        String resultDisease = disease.DeleteUser(supervisor);
        String resultCode = code.DeleteUser(supervisor);
        String resultmedicinepill = medicinepill.DeleteUser(supervisor);
        String resultmedicinepotion = medicinepotion.DeleteUser(supervisor);
        String resultpatient = patient.DeleteUser(supervisor);
        String resulttraditionalpill = traditionalpill.DeleteUser(supervisor);
        String resulttraditionalpotion = traditionalpotion.DeleteUser(supervisor);
        String resultUser = user.DeleteUser(supervisor);
        if(resultDisease.equalsIgnoreCase("1"))
        {
            if(resultmedicinepill.equalsIgnoreCase("1"))
            {
                if(resultmedicinepotion.equalsIgnoreCase("1"))
                {
                    if(resultpatient.equalsIgnoreCase("1"))
                    {
                        if(resulttraditionalpill.equalsIgnoreCase("1"))
                        {
                            if(resulttraditionalpotion.equalsIgnoreCase("1"))
                            {
                                if(resultCode.equalsIgnoreCase("1"))
                                {
                                    if(resultUser.equalsIgnoreCase("1"))
                                    {
                                        JOptionPane.showMessageDialog(rootPane, "ID 删除！");
                                        getComboName();
                                        
                                    }
                                    else
                                    {
                                        JOptionPane.showMessageDialog(rootPane, "ID不能删除！");
                                    }
                                }
                                else
                                {
                                    JOptionPane.showMessageDialog(rootPane, "系统 ID 在 database不能删除！");
                                }
                            }
                            else
                            {
                                JOptionPane.showMessageDialog(rootPane, "复方药粉 ID 在 database不能删除！");
                            }
                        }
                        else
                        {
                            JOptionPane.showMessageDialog(rootPane, "单味药粉 ID 在 database不能删除！");
                        }
                    }
                    else
                    {
                        JOptionPane.showMessageDialog(rootPane, "病人database不能删除！");
                    }
                }
                else
                {
                    JOptionPane.showMessageDialog(rootPane, "药丸 ID 在 database不能删除！");
                }
            }
            else
            {
                JOptionPane.showMessageDialog(rootPane, "药丸 ID 在 database不能删除！");
            }
        }
        else
        {
            JOptionPane.showMessageDialog(rootPane, "病症 ID 在 database不能删除！");
        }
    }
    
    public void getComboName() throws SQLException
    {
        try {
            User usert = new User();
            
            if(usert.comboName().isEmpty())
            {
                comboUser.removeAllItems();
            }
            else
            {
                comboUser.removeAllItems();
                for(int i = 0; i < usert.comboName().size(); i++)
                {
                    comboUser.addItem(usert.comboName().get(i).getUserid());
                }
            }
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "NewPatientDisease.medicineCategory() get error on line 737,"+ex.getMessage());
        }
        
    }
    
